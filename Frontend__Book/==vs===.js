/* 
    Javascript provides two types of equality operator.
    1.==(Loose Equality)
    2.===(Strict Equality)
*/

/* 
Loose Equality Operator(==):
    Compare values after type conversion.
    If types are different,Javascipt tries to convert them before comparison.
    Allows coercion
*/

/* 
Strict Equality Operator(===):
Compares both values and types without conversion.
If types are different it returns false immediately.
doesnot allow coercion
*/

